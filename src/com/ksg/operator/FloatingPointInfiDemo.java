package com.ksg.operator;

public class FloatingPointInfiDemo {

	public static void main(String[] args) {
		
		System.out.println(10/0.0);
		
		System.out.println(-10/0.0);
		
		System.out.println(0/0.0);
		
		System.out.println(0.0/0);
		
		System.out.println(Math.sqrt(4));
		System.out.println(Math.sqrt(-4));
	}


	}


