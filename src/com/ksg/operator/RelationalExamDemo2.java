package com.ksg.operator;

public class RelationalExamDemo2 {

	public static void main(String[] args) {
		
		int marks = 75;

        if (marks >= 90) {
            System.out.println("Grade A+");
        } else if (marks >= 75) {
            System.out.println("Grade A");
        } else if (marks >= 50) {
            System.out.println("Grade B");
        } else {
            System.out.println("Fail");
	}

}}
