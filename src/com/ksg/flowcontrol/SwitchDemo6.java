package com.ksg.flowcontrol;

public class SwitchDemo6 {

	public static void main(String[] args) {
		
		int x = 2;
		switch (x) {
		case 1:
			System.out.println("pizza");
			break;
			
		case 2:
			System.out.println("burger");
			break;
			
		case 3:
			System.out.println("coffee");
			break;
			
		case 4:
			System.out.println("tae");
			break;
		}
	}

}
