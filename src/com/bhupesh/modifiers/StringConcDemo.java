package com.bhupesh.modifiers;

public class StringConcDemo {
	
	public static void main(String[] args) {
		
		int x = 12, y = 22, z = 32;
		
		String s = "Vikash";
		
		System.out.println(x+y+z+s);
		
		System.out.println(x+y+s+z);
		
		System.out.println(x+s+z+y);
		
		System.out.println(s+x+y+z);
	}

}
