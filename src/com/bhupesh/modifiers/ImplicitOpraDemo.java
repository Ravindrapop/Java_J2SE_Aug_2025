package com.bhupesh.modifiers;

public class ImplicitOpraDemo {
	
	public static void main(String[] args) {
		
		byte b = 10;
		
		short s = 20;
		
		int i = 'A';
		
		long l = 40;
		
		float f = 50;
		
		double d = 60;
		
		char c = 97;
		
		System.out.println(b+s+l+f+d+i+c);
		
		System.out.println(c); 
		
		System.out.println(d);
	}

}
