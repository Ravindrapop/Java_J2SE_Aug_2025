package operators.java.pb;

public class ArithmeticOperators {
	public static void main(String [] args) {
		
		int p =60;int q =80;int r=90;
		System.out.println(p+q-r);
		System.out.println(p-q+r);
		System.out.println(p%q*r);
		System.out.println(p*q/r);
		System.out.println(p/q*r);
		System.out.println(p+q+r);
		System.out.println(q-p-r);
		
	}

}
